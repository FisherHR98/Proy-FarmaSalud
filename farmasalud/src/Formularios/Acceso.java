       objEmpleado.setContraseña(ptxtCon.getText());
       
       if ( objConexion.logear(objEmpleado)==1) {
           CUsuario=objConexion.ConUVend(txtUsu.getText());
           CCargo = objConexion.ConUCargo(txtUsu.getText());
            JOptionPane.showMessageDialog(null,"Bienvenido al Sistemas de Facturación");
            objMenu.setVisible(true);
            objMenu.AsignaMenu(CCargo);
            dispose(); 
        }else{
          JOptionPane.showMessageDialog(null,"Acceso Denegado"); 
        }
       

