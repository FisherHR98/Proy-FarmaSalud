void GraficoCircular(int an){
    DefaultPieDataset ds=new DefaultPieDataset();
    for(ReciboGraf x:obj.LisRecibo(an)){
        ds.setValue(x.getCod_ven()+"-"+x.getNom_ven(),x.getMonto());
    }
    JFreeChart gf=ChartFactory.createPieChart3D("Ventas "+an, ds);
        ChartPanel graf= new ChartPanel(gf);
        jPanel1.removeAll();//Limpiar el control panel
        jPanel1.setLayout(new java.awt.BorderLayout());
        jPanel1.add(graf, BorderLayout.CENTER);
        jPanel1.validate();
}
