    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        botones(2);
        desactivar();
        
        String est="";
        String Nom_dis=txtNom.getText();
        String cod_dis=txtCod.getText();
        if (chkEst.isSelected()==true){
            est=("1");
        }else{
            est=("0");
        }
        Distrito di = new Distrito(cod_dis,Nom_dis,est);
        objDistrito.AdicionDis(di);

        limpiar();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        botones(3);
        activar();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        botones(4);
        Distrito di=new Distrito();
        
        di.setCodigoDis(txtCod.getText());
        di.setNombreDis(txtNom.getText());       
        if (chkEst.isSelected()==true){
            di.setEstado("1");
        }else{
            di.setEstado("0");
        }

        objDistrito.actualizaDi(di);
        desactivar();
        limpiar();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       Distrito di = new Distrito();
       di.setCodigoDis(txtCod.getText());
       di.setEstado("0");
       objDistrito.eliminarDis(di);
       muestra();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        botones(6);
        desactivar();
        limpiar();
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void jtDisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtDisMouseClicked
       int fila=jtDis.getSelectedRow();
       txtCod.setText(jtDis.getValueAt(fila, 0).toString());
       txtNom.setText(jtDis.getValueAt(fila, 1).toString());
       if(jtDis.getValueAt(fila, 2).equals("1")){
            chkEst.setSelected(true);
        }else{
            chkEst.setSelected(false);
        }
    }//GEN-LAST:event_jtDisMouseClicked
void activar(){
    txtCod.setEditable(true);
    txtNom.setEditable(true);
    chkEst.setEnabled(true);
    txtCod.requestFocus();
}
void desactivar(){
    txtCod.setEditable(false);
    txtNom.setEditable(false);
    chkEst.setEnabled(false);
    txtCod.requestFocus();
}

void limpiar(){
    txtCod.setText("");
    txtNom.setText("");
    txtCod.requestFocus();
}
public void botones(int i){
    switch(i){
        case 1:
            btnNuevo.setEnabled(false);
            btnGuardar.setEnabled(true);
            btnEditar.setEnabled(false);
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnCancelar.setEnabled(true);
            break;
        case 2:
            btnNuevo.setEnabled(true);
            btnGuardar.setEnabled(false);
            btnEditar.setEnabled(true);
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(true);
            btnCancelar.setEnabled(false);
            break;                
        case 3:
            btnNuevo.setEnabled(false);
            btnGuardar.setEnabled(false);
            btnEditar.setEnabled(false);
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(false);
            btnCancelar.setEnabled(true);
            break;
        case 4: 
            btnNuevo.setEnabled(true);
            btnGuardar.setEnabled(false);
            btnEditar.setEnabled(true);
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(true);
            btnCancelar.setEnabled(false);
            break;
        case 5:
            break;
        case 6:
            btnNuevo.setEnabled(true);
            btnGuardar.setEnabled(false);
            btnEditar.setEnabled(true);
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(true);
            btnCancelar.setEnabled(false);
            break;        
    }
}
